function fizzBuzz(num) {
  for ( var num = 1; num < 16; num ++ ) {
  
    var checkForThree = num % 3;
    var checkForFive = num % 5;
    
    
    if ( ( checkForThree == 0 && checkForFive == 0) ) 
      console.log( "fizzbuzz");
    
   
    else if (checkForThree == 0)
      console.log( "fizz");
    
    
    else if (checkForFive == 0)
      console.log( "buzz");
    
   
    else 
      console.log(num);
  } 
}
  

